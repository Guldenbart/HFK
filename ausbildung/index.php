				Das Ausbildungskonzept der HFK:
								<br><br>
				Wie viele andere Musikvereine hatten auch die Harmonika-Freunde zunehmend das Problem, ausreichenden Nachwuchs an aktiven, jungen Spielern für das Akkordeon zu gewinnen.
				In den 90er Jahren hat der deutsche Harmonika-Verband (DHV) mit dem Kultusministerium Baden-Württemberg das Projekt "Kooperation Schule - Verein" entwickelt. Bei dem Projekt gehen Schule und Verein zum beiderseitigen Nutzen eine Kooperation ein. Der Verein erteilt in der Schule und integriert in den Schulstundenplan im Rahmen einer freiwilligen Arbeitsgemeinschaft (AG) kostenlos Musikunterricht. Er übernimmt damit die Unterrichtsausbildung im Fach Musik. Für sein Engagement wird der Verein in den ersten Jahren einer Kooperation vom Kultusministerium finanziell gefördert. Anschließend muss sich das Projekt selbst tragen.<br>
				Jörg Kuchenbecker und Rainer Lehmann, musikalischer Leiter und ausgebildeter Musikpädagoge, waren sofort bereit sich für diese Modell einzusetzen. Bis zur Umsetzung waren aber noch viele spannende Fragen zu klären. <br>
				Woher kommen die Instrumente; wie werden die Instrumente und der für die Schüler kostenlose Unterricht finanziert; mit welchen Grundschulen, in welcher Klassenstufe kann der HFK die Kooperation eingehen? Und ganz wichtig: wie können wir erreichen, dass nach dem Akkordeonunterricht in der Schule viele Schüler weiterhin in der privaten Musikschule von Rainer Lehmann den Unterricht fortsetzen und als neue Spieler in den HFK und ein neu zu gründendes Schülerorchester eintreten? <br>
				Die Instrumentenfrage konnte mit dem Musikhaus Pietsch gelöst werden. Im Wege des Mietkaufs können wir jedes Jahr die Instrumente mieten und zum Ablauf der Mietdauer unter Anrechnung der gezahlten Mieten auf den Kaufpreis, die Instrumente zum Ende der Mietdauer kaufen. Dieses Finanzierungsmodell wird auch gleichzeitig im Verhältnis zu den Eltern der Kooperationsschüler angewendet. So können die Eltern preiswert ein Instrument mieten und es erwerben, wenn ihr Kind nach der AG den Unterricht fortsetzt; andernfalls wird das Instrument an den HFK und letztlich an das Musikhaus zurückgegeben. <br>
				Der HFK schloss mit Rainer Lehmann eine Vereinbarung, dass er in den Schulen im Auftrag und Namen des HFK die Akkordeon-AGs umsetzt. Weiterhin wurde vereinbart, wie nach Ablauf der AG in der Schule die Schüler in seiner eigenen Musikschule den Unterricht fortsetzen können und bei entsprechendem Ausbildungsstand in unser neues Schülerorchester integriert und als neue Vereinsmitglieder gewonnen werden. <br>
				Im Jahr 2004 wurden die ersten Anfragen bei den Grundschulen in Wollmatingen und Litzelstetten gestellt. Beide Schulleiterinnen sagten ihre Unterstützung und den Abschluss der notwendigen Kooperationsvereinbarung zu. Im Frühjahr 2005 stellte Rainer Lehmann in den Klassen das Instrument Akkordeon vor und machte so die Schulkinder auf die kommende Musik-AG aufmerksam und neugierig. Die Eltern der Kinder wurden danach in einem Elternabend über das Projekt informiert und erhielten die Anmeldeformulare für die Akkordeon-AG ausgehändigt. <br>
				Zu Beginn des Schuljahres 2005/2006 lagen 43 Anmeldungen vor. Zeitgleich erhielten wir vom DHV die finanzielle Förderzusage des Kultusministeriums und die Kooperationsurkunden ausgehändigt. Die Hälfte der Kinder hat nach dem Kooperationsjahr ihren Gruppenunterricht in der Musikschule von Rainer Lehmann fortgesetzt und mit 15 von ihnen konnte ein neues Schülerorchester gegründet werden. Damit war der Neustart in der Nachwuchsarbeit der Harmonika-Freunde zum Erfolg geworden. <br>
				Zu Beginn des Schuljahres 2008/2009 gelang es die Kooperation in der Grundschule Haidelmoos und ein Jahr darauf auch in der Berchenschule zu starten. Inzwischen ist das Schülerorchester aus 2006 als neues Jugendorchester aufgerückt und im Herbst 2011 hat die Probenarbeit mit einem neuen Schülerorchester begonnen. <br>
				Der finanzielle und organisatorische Aufwand mit den Schulkooperationen ist für den Verein beachtlich, insgesamt aber erfolgreich. Schließlich kann heute nicht mehr jeder Verein eine funktionierende Nachwuchsarbeit für die eigene Zukunftssicherung vorweisen.
